            //移动端
            $('.feature-item').on('click', function(){
                $(this).find('.icon-slide').toggleClass('icon-up');
                $(this).siblings('.feature-item-detail').slideToggle();
            });

            //弹出窗口
            $('.mobile-apply-button').on('click', function(e){
                console.log('23');
                Custombox.open({
                    target: '.mobile-modal',
                    effect: 'fadein',
                    speed: 300,
                });
                e.preventDefault();
            });
            $('#form-new-branch').submit(function ($event) {
                var $this = $(this)
                var branch_name = $.trim($('#branch_name').val())
                var owner_name = $.trim($('#owner_name').val())
                var owner_mobile = $.trim($('#owner_mobile').val())
                var captcha = $.trim($('#captcha').val())
                $event.preventDefault()
                if (!branch_name || !owner_name || !owner_mobile || !captcha) {
                    alert('请先填写信息和验证码')
                    return
                }
                $.post($(this).attr('action'), {
                    branch_name: branch_name,
                    owner_name: owner_name,
                    owner_mobile: owner_mobile,
                    captcha: captcha
                }).then(function (data) {
                    if (data.err == 0) {
                        $this.find('input').prop('disabled', true)
                        alert('提交成功')
                        Custombox.close()
                    } else if (data.err == 'captcha') {
                        $('#captcha').val('')
                        $('#img-captcha').click()
                        alert(data.msg)
                    } else {
                        alert(data.msg)
                    }
                })
            })
            $('#img-captcha').click(function () {
                var $this = $(this)
                $this.attr('src', $this.data('url') + '?r=' + Math.random())
            }).click()
